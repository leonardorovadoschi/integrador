public class EntidadeAgis {
    private String partNumber;
    private String descricao;
    private String precoSP;
    private String precoES;
    private String precoRJ;

    public String getPartNumber() {
        return partNumber;
    }

    public void setPartNumber(String partNumber) {
        this.partNumber = partNumber;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getPrecoSP() {
        return precoSP;
    }

    public void setPrecoSP(String precoSP) {
        this.precoSP = precoSP;
    }

    public String getPrecoES() {
        return precoES;
    }

    public void setPrecoES(String precoES) {
        this.precoES = precoES;
    }

    public String getPrecoRJ() {
        return precoRJ;
    }

   public void setPrecoRJ(String precoRJ) {
       this.precoRJ = precoRJ;
    }
    
    
}
