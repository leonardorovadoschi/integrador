    private void cancelamento(){
        int cancelar = JOptionPane.showConfirmDialog(null, " Deseja realmente cancelar? \n O processo será encerrado!!", "Cancelar", JOptionPane.YES_NO_CANCEL_OPTION);
            if (cancelar == JOptionPane.YES_OPTION) {
                setCancelamento(true);               
                dispose();
            }
    }
    
    private void finalizacao(){
        colunaCodCli = jTablelistagemClientes.getColumnModel().getColumnIndex("Codcli");
        if (jTablelistagemClientes.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Você deve selecionar uma linha na tabela!!! ");
        } else {
            String cod = jTablelistagemClientes.getValueAt(jTablelistagemClientes.getSelectedRow(), colunaCodCli).toString();
            if (cod != null) {
                setCliente(new ClienteJpaController(managerCplus).findCliente(cod));
                setCancelamento(false);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "O Codigo está nullo por favor verifique!!! ");
                setCancelamento(true);
            }
        }
    }
    
    public void listarClientes() {
        clienteList.clear();
        switch (jComboBoxTipoPesquisa.getSelectedIndex()) {
            case 0:
                if ("".equals(jTextFieldTermoPesquisa.getText())) {
                    JOptionPane.showMessageDialog(null, "Campo nulo!!! ");
                } else {
                    List<Cliente> listCliente = queryCplus.listagemClientePorQualquerParteNome(jTextFieldTermoPesquisa.getText());
                    for (Cliente cli : listCliente) {
                        clienteList.add(cli);
                    }
                }
            break;
            case 1:
                if ("".equals(jTextFieldTermoPesquisa.getText())) {
                    JOptionPane.showMessageDialog(null, "Campo Deve conter o CNPJ ou CPF!!! ");
                } else {
                    String str = jTextFieldTermoPesquisa.getText().trim();
                    str = str.replaceAll("[^0-9]", ""); //somente numeros
                    jTextFieldTermoPesquisa.setText(str);
                    List<Cliente> listCliente = queryCplus.resultPortCnpjOuCpf(jTextFieldTermoPesquisa.getText());
                    for (Cliente cli : listCliente) {
                        clienteList.add(cli);
                    }
                }
                break;
                case 2:
                if ("".equals(jTextFieldTermoPesquisa.getText())) {
                    JOptionPane.showMessageDialog(null, "Campo Deve conter o CNPJ ou CPF!!! ");
                } else {
                    String str = jTextFieldTermoPesquisa.getText().trim();
                    //str = str.replaceAll("[^0-9]", ""); //somente numeros
                    jTextFieldTermoPesquisa.setText(str);
                    List<Cliente> listCliente;
                    if(jCheckBoxSomenteCpf.isSelected()){
                        listCliente = queryCplus.resultUfCliente(jTextFieldTermoPesquisa.getText().toUpperCase(), 'Y');
                    }else{
                    listCliente = queryCplus.resultUfCliente(jTextFieldTermoPesquisa.getText().toUpperCase());
                    }
                    for (Cliente cli : listCliente) {
                        clienteList.add(cli);
                    }
                }
                break;
        }
    }

