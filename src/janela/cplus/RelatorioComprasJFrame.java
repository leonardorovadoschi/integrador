    private int estoqueCplus(String codProduto) {
        int estoqueDisponivel = 0;
        List<Produtoestoque> listEsroque = queryCplus.listProdutoEstoque(codProduto);
        for (Produtoestoque est : listEsroque) {
             estoqueDisponivel = est.getEstatu().intValue() - est.getReservadoorcamento().intValue() - est.getReservadoos().intValue();
        }
        return estoqueDisponivel;
    }

    private double mediaVendaProduto(String codProdCplus) {
        BigDecimal totalVendas = BigDecimal.ZERO;
        numerodeDiasCalculo = BigDecimal.ZERO;
        boolean comEstoque = false;
        List<Movendaprod> listaVendaProdData;
        List<Moventradaprod> listEntradaProdData = queryCplus.resultProdutoEntrada(codProdCplus, 'C', jDateChooserDataInicial.getDate(), jDateChooserDataFinal.getDate());
        List<Produtoestoque> listEsroque = queryCplus.listProdutoEstoque(codProdCplus);
        for (Produtoestoque est : listEsroque) {
            int estoqueDisponivel = est.getEstatu().intValue() - est.getReservadoorcamento().intValue() - est.getReservadoos().intValue();
            if (estoqueDisponivel > 0) {
                comEstoque = true;
            }
        }
        if (listEntradaProdData.isEmpty()) {
            List<Moventradaprod> listEntradaProd = queryCplus.resultProdutoEntrada(codProdCplus, true, 1);
            Date primeiraDataCompra = jDateChooserDataFinal.getDate();
            for (Moventradaprod entradaProd : listEntradaProd) {
                if (comparaDatas(entradaProd.getCodmoventr().getData(), primeiraDataCompra)) {
                    primeiraDataCompra = entradaProd.getCodmoventr().getData();
                }
            }
            if (comEstoque) {
                listaVendaProdData = queryCplus.resultProdutoVenda(codProdCplus, 'V', primeiraDataCompra, jDateChooserDataFinal.getDate());
                numerodeDiasCalculo = diferencaDeDias(primeiraDataCompra, jDateChooserDataFinal.getDate());
                inicioAvaliacao = primeiraDataCompra;
                fimAvaliacao = jDateChooserDataFinal.getDate();
                for (Movendaprod vendaProd : listaVendaProdData) {
                    totalVendas = totalVendas.add(vendaProd.getQuantidade());
                }
            } else {
                List<Movendaprod> listaVendaProd = queryCplus.resultProdutoVenda(codProdCplus, true, 1);
                for (Movendaprod prodUltimo : listaVendaProd) {
                    listaVendaProdData = queryCplus.resultProdutoVenda(codProdCplus, 'V', primeiraDataCompra, prodUltimo.getCodmovenda().getData());
                    numerodeDiasCalculo = diferencaDeDias(primeiraDataCompra, prodUltimo.getCodmovenda().getData());
                    inicioAvaliacao = primeiraDataCompra;
                    fimAvaliacao = prodUltimo.getCodmovenda().getData();
                    for (Movendaprod vendaProd : listaVendaProdData) {
                        totalVendas = totalVendas.add(vendaProd.getQuantidade());
                    }
                }
            }
        } else {
            if (comEstoque) {
                listaVendaProdData = queryCplus.resultProdutoVenda(codProdCplus, 'V', jDateChooserDataInicial.getDate(), jDateChooserDataFinal.getDate());
                numerodeDiasCalculo = diferencaDeDias(jDateChooserDataInicial.getDate(), jDateChooserDataFinal.getDate());
                inicioAvaliacao = jDateChooserDataInicial.getDate();
                fimAvaliacao = jDateChooserDataFinal.getDate();
                for (Movendaprod vendaProd : listaVendaProdData) {
                    totalVendas = totalVendas.add(vendaProd.getQuantidade());
                }
            } else {
                List<Movendaprod> listaVendaProd = queryCplus.resultProdutoVenda(codProdCplus, true, 1);
                for (Movendaprod prodUltimo : listaVendaProd) {
                    listaVendaProdData = queryCplus.resultProdutoVenda(codProdCplus, 'V', jDateChooserDataInicial.getDate(), prodUltimo.getCodmovenda().getData());
                    numerodeDiasCalculo = diferencaDeDias(jDateChooserDataInicial.getDate(), prodUltimo.getCodmovenda().getData());
                    inicioAvaliacao = jDateChooserDataInicial.getDate();
                    fimAvaliacao = prodUltimo.getCodmovenda().getData();
                    for (Movendaprod vendaProd : listaVendaProdData) {
                        totalVendas = totalVendas.add(vendaProd.getQuantidade());
                    }
                }
            }
        }
        BigDecimal totBig;
        if (numerodeDiasCalculo.doubleValue() == 0.00) {
            totBig = totalVendas;
        } else {
            totBig = totalVendas.divide(numerodeDiasCalculo, 3, RoundingMode.UP);
        }
        //BigDecimal totBig = new BigDecimal(tot).setScale(3, BigDecimal.ROUND_HALF_UP);
        return totBig.doubleValue();
    }

    private BigDecimal diferencaDeDias(Date primeiraData, Date segundaData) {
        int nDias = 0;
        Calendar segundaDataCalendar = Calendar.getInstance();
        Calendar primeiraDataCalendar = Calendar.getInstance();
        segundaDataCalendar.setTime(segundaData);
        primeiraDataCalendar.setTime(primeiraData);
        nDias = (segundaDataCalendar.get(Calendar.YEAR) * 365 + segundaDataCalendar.get(Calendar.DAY_OF_YEAR)) - (primeiraDataCalendar.get(Calendar.YEAR) * 365 + primeiraDataCalendar.get(Calendar.DAY_OF_YEAR));
        return new BigDecimal(nDias);
    }

    private boolean comparaDatas(Date primeiraData, Date segundaData) {
        boolean condicao = false;
        Calendar segundaDataCalendar = Calendar.getInstance();
        Calendar primeiraDataCalendar = Calendar.getInstance();
        segundaDataCalendar.setTime(segundaData);
        primeiraDataCalendar.setTime(primeiraData);
        String diaAtualStr = String.format("%02d", segundaDataCalendar.get(Calendar.DAY_OF_MONTH));
        String mesAtualStr = String.format("%02d", segundaDataCalendar.get(Calendar.MONTH));
        String anoAtualStr = String.format("%04d", segundaDataCalendar.get(Calendar.YEAR));
        int totalSegundaData = Integer.valueOf(anoAtualStr + mesAtualStr + diaAtualStr);

        String diaBancoStr = String.format("%02d", primeiraDataCalendar.get(Calendar.DAY_OF_MONTH));
        String mesBancoStr = String.format("%02d", primeiraDataCalendar.get(Calendar.MONTH));
        String anoBancoStr = String.format("%04d", primeiraDataCalendar.get(Calendar.YEAR));
        int totalPrimeiraData = Integer.valueOf(anoBancoStr + mesBancoStr + diaBancoStr);
        if (totalSegundaData <= totalPrimeiraData) {
            condicao = false;
        } else {
            condicao = true;
        }
        return condicao;
    }

