    private void jTableRelatorioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableRelatorioMouseClicked
        idProdCplus = jTableRelatorio.getValueAt(jTableRelatorio.getSelectedRow(), colunaCodprod).toString();
        produtoCplus = new ProdutoJpaController(managerCplus).findProduto(idProdCplus);
        if (produtoCplus.getDatreaj() != null) {
            // jTextFieldDataUltimoReajuste.setText(formataCampo.dataStringSoData(produtoCplus.getDatreaj(), 0));
        }
        if(produtoCplus.getValoripi() != null){
        jTextFieldPrecoCusto.setText(formataCampo.bigDecimalParaString(produtoCplus.getPrecusto().add(produtoCplus.getValoripi()), 2));
        }
        jTextFieldCustoReal.setText(formataCampo.bigDecimalParaString(produtoCplus.getCustoreal(), 2));
        //jTextFieldValorIpi.setText(formataCampo.bigDecimalParaString(produtoCplus.getValoripi(), 2));
        jTextFieldValorSubstituicaoTributaria.setText(formataCampo.bigDecimalParaString(produtoCplus.getValorsubsttributaria(), 2));
        jTextFieldPercOutrosCustos.setText(formataCampo.bigDecimalParaString(produtoCplus.getPercoutroscustos(), 2));
        char compra = 'C';
        List<Moventrada> listMovEntrada = queryCplus.resultUltimaCompra(idProdCplus, compra, 1);
        for (Moventrada movEnt : listMovEntrada) {
            jTextFieldDataUltimaCompra.setText(formataCampo.dataStringSoData(movEnt.getData(), 0));
            if (movEnt.getCodForn() != null) {
                jTextFieldFornecedor.setText(movEnt.getCodForn().getNomeforn());
            }
            for (Moventradaprod movPro : queryCplus.resultProdutoEntrada(idProdCplus, movEnt.getCodmoventr())) {

                jTextFieldUltimaQuantidadeComprada.setText(formataCampo.bigDecimalParaString(movPro.getQuantidade(), 0));

            }
        }
        char venda = 'V';
        List<Movenda> listMovVenda = queryCplus.resultUltimaVenda(idProdCplus, venda, 1);
        for (Movenda movVen : listMovVenda) {
            jTextFieldDataUltimaVenda.setText(formataCampo.dataStringSoData(movVen.getData(), 0));
        }
        for (Produtoestoque estoque : queryCplus.resultTodosEstoques(idProdCplus)) {
            if ("000000001".equals(estoque.getSetorestoque().getCodsetorestoque())) {
                BigDecimal estoqueDisponivel = estoque.getEstatu().subtract(estoque.getReservadoorcamento()).subtract(estoque.getReservadoos());
                jTextFieldEstoqueDisponivel.setText(formataCampo.bigDecimalParaString(estoqueDisponivel, 0));
            }
        }
        produtosAllnationsListProdutos.clear();
        for (ProdFornecedor proAll : queryIntegrador.listProdFornecedor(idProdCplus, 1)) {
            produtosAllnationsListProdutos.add(proAll);
        }
        limpaCampos();
    }//GEN-LAST:event_jTableRelatorioMouseClicked

    private void jButtonCalculaSujestaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalculaSujestaoActionPerformed
        if (idProdCplus != null) {
            for (Produto prodInt : queryCplus.listProduto(idProdCplus)) {
                double sugestao = 0.00;
                double mediaDiaria;
                mediaDiaria = mediaVendaProduto(prodInt.getCodprod());
                sugestao = mediaDiaria * Double.valueOf(jTextFieldNumeroDiasCompra.getText());
                sugestao = sugestao;
                if (sugestao < 1.00) {
                    sugestao = 0.00;
                }
                jTextFieldSujestao.setText(String.valueOf((int) sugestao));
                jDateChooserInicioAvaliacao.setDate(inicioAvaliacao);
                jDateChooserFimAvaliacao.setDate(fimAvaliacao);
                jTextFieldMediaDiaria.setText(formataCampo.bigDecimalParaString(new BigDecimal(mediaDiaria).setScale(3, BigDecimal.ROUND_HALF_UP), 3));
                jTextFieldNumeroDiasCalculo.setText(formataCampo.bigDecimalParaString(numerodeDiasCalculo, 0));
            }
        }
    }//GEN-LAST:event_jButtonCalculaSujestaoActionPerformed

